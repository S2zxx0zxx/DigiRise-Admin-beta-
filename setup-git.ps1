# Git Setup & Push Script for Phase 0

# Initialize repository
git init

# Add all files
git add .

# Initial commit
git commit -m "Initialize project skeleton and Firebase wiring (Phase 0)"

# Set remote origin
git remote add origin https://github.com/S2zxx0zxx/DigiRise-Admin-beta-.git

# Rename branch to main
git branch -M main

# Push to Github
git push -u origin main
